package com.bus.vo;

public class BusCheckVO {
	
	private String bd_id;
	private String bb_day;
	private String bb_time;
	private String bb_start;
	private String bb_end;
	private String bb_seat;
	private int bb_price;
	
	public String getBd_id() {
		return bd_id;
	}
	public void setBd_id(String bd_id) {
		this.bd_id = bd_id;
	}
	public String getBb_day() {
		return bb_day;
	}
	public void setBb_day(String bb_day) {
		this.bb_day = bb_day;
	}
	public String getBb_time() {
		return bb_time;
	}
	public void setBb_time(String bb_time) {
		this.bb_time = bb_time;
	}
	public String getBb_start() {
		return bb_start;
	}
	public void setBb_start(String bb_start) {
		this.bb_start = bb_start;
	}
	public String getBb_end() {
		return bb_end;
	}
	public void setBb_end(String bb_end) {
		this.bb_end = bb_end;
	}
	public String getBb_seat() {
		return bb_seat;
	}
	public void setBb_seat(String bb_seat) {
		this.bb_seat = bb_seat;
	}
	public int getBb_price() {
		return bb_price;
	}
	public void setBb_price(int bb_price) {
		this.bb_price = bb_price;
	}

}
